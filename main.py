# Инструкция по работе с Git

# Установка Git
# Windows
# 1. Скачайте установщик с официального сайта Git: https://git-scm.com/downloads.
# 2. Запустите установщик и следуйте инструкциям.

# macOS
# Используйте Homebrew для установки Git:
# brew install git

# Linux
# Используйте пакетный менеджер вашего дистрибутива:
# sudo apt-get install git  # для Debian/Ubuntu
# sudo yum install git      # для CentOS/RHEL

# Настройка Git
# После установки Git выполните первоначальную настройку:
# git config --global user.name "Ваше Имя"
# git config --global user.email "ваш.email@example.com"

# Основные команды Git
# Создание нового репозитория
# git init

# Клонирование существующего репозитория
# git clone https://github.com/USERNAME/REPOSITORY.git

# Добавление файлов в индекс (staging area)
# git add <имя_файла>
# git add .  # Добавить все файлы

# Создание коммита
# git commit -m "Сообщение коммита"

# Просмотр статуса репозитория
# git status

# Просмотр истории коммитов
# git log

# Работа с удаленным репозиторием
# Добавление удаленного репозитория
# git remote add origin https://github.com/USERNAME/REPOSITORY.git

# Отправка изменений в удаленный репозиторий
# git push origin main

# Получение изменений из удаленного репозитория
# git pull origin main

# Просмотр списка удаленных репозиториев
# git remote -v

# Добавление нового удаленного репозитория
# git remote add <название> <url>

# Удаление удаленного репозитория
# git remote remove <название>

# Переименование удаленного репозитория
# git remote rename <старое_название> <новое_название>

# Получение информации о удаленном репозитории
# git remote show <название>

# Работа с ветками
# Создание новой ветки
# git checkout -b имя_ветки

# Переключение между ветками
# git checkout имя_ветки

# Слияние веток
# git merge имя_ветки

# Удаление ветки
# git branch -d имя_ветки

# Работа с Pull Request
# Форк репозитория
# 1. Перейдите на страницу репозитория на GitHub.
# 2. Нажмите кнопку "Fork" в правом верхнем углу.

# Клонирование форкнутого репозитория
# git clone https://github.com/ВАШ_ЮЗЕРНЕЙМ/REPOSITORY.git

# Создание новой ветки для изменений
# git checkout -b имя_ветки

# Внесение изменений и коммит
# 1. Внесите изменения в код.
# 2. Добавьте файлы в индекс:
#    git add .
# 3. Создайте коммит:
#    git commit -m "Описание изменений"

# Отправка изменений в ваш форк на GitHub
# git push origin имя_ветки

# Создание Pull Request
# 1. Перейдите на страницу вашего форка на GitHub.
# 2. Нажмите кнопку "Compare & pull request".
# 3. Выберите ветку оригинального репозитория и вашу ветку для сравнения.
# 4. Заполните информацию и нажмите "Create pull request".

